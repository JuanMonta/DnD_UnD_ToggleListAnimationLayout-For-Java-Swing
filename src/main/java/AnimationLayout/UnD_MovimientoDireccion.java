package AnimationLayout;

/**
 * Enum para representar la dirección del movimiento de animaciones de items y
 * subItems.
 */
public class UnD_MovimientoDireccion {

    /**
     * Representación del movimiento hacia ARRIBA de la animación de items y
     * subItems.
     */
    public static final int UP = -1;
    /**
     * Representación del movimiento hacia ABAJO de la animación de items y
     * subItems.
     */
    public static final int DOWN = 1;

}
